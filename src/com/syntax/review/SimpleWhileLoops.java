package com.syntax.review;

public class SimpleWhileLoops {

	public static void main(String[] args) {

		
		System.out.println("--------------------Increment-------------------");
		int var=0;
		while(var<=10) {
			System.out.println("Value of var = ");
			var++;
		}

		System.out.println("--------------------Increment-------------------");
		var=10;
		while(var>10) {
			System.out.println("value of var = ");
			var--;
		}
		
	}

}
