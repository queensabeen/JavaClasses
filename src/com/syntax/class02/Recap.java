package com.syntax.class02;

public class Recap {

	public static void main(String[] args) {
		byte smallnum = 3;
		short midnum = 123;
		int largenum = 1234565;
		long xlnum = 123446677L;
		
		//to store floating numbers
		float var5 = 1.6f;
		double var6 = 12.9999;
		
		//to store yes or no (true or false)
		boolean boo = true;
		boolean boo2 = false;
		
		//to store single characters
		char mybox = 'c';
		char mybox2 = '&';
		char mybox3 = '7';

		System.out.println(smallnum);
		System.out.println(midnum);
		System.out.println(largenum);
		System.out.println(xlnum);
		System.out.println(var5);
		System.out.println(var6);
		System.out.println(boo);
		System.out.println(boo2);
		System.out.println(mybox);
		System.out.println(mybox2);
		System.out.println(mybox3);
		
		//adding system.out.println(); will create a new line
		
		//system.out.println(huge); - error because the code executes top to bottom. 
		int huge = 5000;

	}

}
